function loadChapters() {
    const buttonContainer = document.getElementById('chapter-buttons');
    
    // Loop through buttonData to create each button
    buttonData.forEach(data => {
        const button = document.createElement('a');  // Create a new anchor (button)
        button.href = data.link;  // Set the link
        button.textContent = data.label;  // Set the button label (e.g., Chapter 1, Chapter 2)
        button.className = "inline-block w-40 h-25 px-6 py-4 m-2 bg-gray-800 rounded-lg hover:bg-gray-900 transition duration-300";
        buttonContainer.appendChild(button);  // Append button to the container
    });
}

window.onload = loadChapters;  // Call loadChapters when the page loads


      
